#include "ft_printf.h"

int	ft_num(int i)
{
	int	k;

	k = 0;
	if (i == -2147483648)
	{
		k += ft_str("-2147483648");
		return (k);
	}
	if (i < 0)
	{
		k += ft_putchar('-');
		i = -i;
	}
	if (i > 9)
	{
		k += ft_num(i / 10);
	}
	k += ft_putchar(i % 10 + '0');
	return (k);
}
